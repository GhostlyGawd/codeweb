// -------------------------------------------------------------------------------------------------
// INDEPENDENT lexical shingler (a parallel reimplementation; deliberately NOT scripts/lib/shingles.mjs)
// — used to sanity-rank lexical similarity in-process for H10 without borrowing the tool's code.
// -------------------------------------------------------------------------------------------------
const KW = new Set(['if', 'for', 'while', 'switch', 'catch', 'return', 'function', 'typeof', 'await', 'new', 'const', 'let', 'var', 'async', 'else', 'try', 'finally', 'class', 'case', 'of', 'in', 'throw']);
const lexTokens = (src) => (src || '')
  .replace(/\/\/[^\n]*/g, ' ').replace(/\/\*[\s\S]*?\*\//g, ' ')
  .replace(/(['"`])(?:\\.|(?!\1).)*\1/g, ' STR ').toLowerCase()
  .match(/[a-z_$][\w$]*|[{}();=><!+\-*/%]/g)?.filter((t) => !KW.has(t)) || [];
const lexShingles = (src, k = 3) => { const t = lexTokens(src); const s = new Set(); for (let i = 0; i + k <= t.length; i++) s.add(t.slice(i, i + k).join(' ')); return s; };
const jacc = (a, b) => { if (!a.size || !b.size) return 0; let i = 0; for (const x of a) if (b.has(x)) i++; return i / (a.size + b.size - i); };

// -------------------------------------------------------------------------------------------------
// Deterministic source synthesis. Every body is a REAL, parseable JS function written to disk; the
// generator records EXACT ground truth. Bodies are built from a seeded token vocabulary so distinct
// functions are lexically distant and clones are exact.
// -------------------------------------------------------------------------------------------------
const VARS = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'm', 'n', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z'];
const FIELDS = ['value', 'count', 'price', 'name', 'id', 'cost', 'qty', 'rate', 'total', 'len', 'size', 'key', 'idx'];
const FNS = ['parse', 'format', 'normalize', 'collect', 'reduce', 'apply', 'derive', 'resolve', 'merge', 'split', 'encode', 'decode'];
const pick = (rng, arr) => arr[(rng() * arr.length) | 0];
const ipick = (rng, lo, hi) => lo + ((rng() * (hi - lo + 1)) | 0);

// A structurally-rich, deterministic function body keyed by a seed. Returns the JS source of a
// function `name` whose body uses `nStmts` statements drawn from the vocab — lexically distinctive.
function makeBody(rng, name, nStmts) {
  const lines = [`function ${name}(${pick(rng, VARS)}, ${pick(rng, VARS)}) {`];
  let acc = pick(rng, VARS);
  lines.push(`  let ${acc} = ${ipick(rng, 0, 9)};`);
  for (let i = 0; i < nStmts; i++) {
    const kind = ipick(rng, 0, 3);
    const v = pick(rng, VARS), fld = pick(rng, FIELDS), fn = pick(rng, FNS), num = ipick(rng, 1, 99);
    if (kind === 0) lines.push(`  for (const ${v} of ${pick(rng, VARS)}) { ${acc} += ${v}.${fld} * ${num}; }`);
    else if (kind === 1) lines.push(`  if (${acc} > ${num}) { ${acc} = ${fn}(${acc}, ${num}); }`);
    else if (kind === 2) lines.push(`  const ${v} = ${fn}(${acc}).${fld};`);
    else lines.push(`  ${acc} = ${acc} + ${v.length ? v : 'x'}.${fld};`);
  }
  lines.push(`  return ${acc};`);
  lines.push('}');
  return lines.join('\n');
}

// Rename every identifier in a JS function body (Type-2 transform). Keeps keywords/operators, maps
// each distinct identifier to a fresh distinct name, and rewrites numeric literals — so the lexical
// token stream changes substantially while the STRUCTURE (skeleton) is preserved. Independent of
// codeweb's skeleton lib (this is a source-level rewrite, not a skeleton compare).
const JS_RESERVED = new Set(['function', 'let', 'const', 'var', 'for', 'of', 'in', 'if', 'else', 'return', 'while', 'do', 'switch', 'case', 'break', 'continue', 'new', 'class', 'extends', 'true', 'false', 'null', 'this', 'typeof', 'instanceof', 'await', 'async']);
function renameBody(src, salt) {
  let counter = 0; const map = new Map();
  const renamed = src.replace(/[A-Za-z_$][\w$]*/g, (tok) => {
    if (JS_RESERVED.has(tok)) return tok;
    if (!map.has(tok)) map.set(tok, `${tok[0] === '_' ? 'z' : 'q'}${salt}_${counter++}`);
    return map.get(tok);
  });
  // bump numeric literals so lexical shingles diverge further (structure unchanged)
  return renamed.replace(/\b(\d+)\b/g, (_, d) => String((Number(d) + 7 + salt) % 100));
}

function freshTmp(prefix) { return mkdtempSync(join(tmpdir(), prefix)); }
function writeFile(rootDir, rel, content) { const abs = join(rootDir, rel); mkdirSync(dirname(abs), { recursive: true }); writeFileSync(abs, content); }
function rmrf(dir) { try { rmSync(dir, { recursive: true, force: true }); } catch { /* best effort */ } }

// Run the deterministic pipeline (extract -> cluster -> overlap) over a source dir into a fresh WS.
// Returns the post-overlap graph object.
function runPipeline(srcDir, target, env = {}) {
  const ws = freshTmp('cw-c4-ws-');
  const e = { ...process.env, ...env, CODEWEB_WS: ws };
  execFileSync(NODE, [S('extract-symbols.mjs'), srcDir, '--no-ctags', '--target', target, '--out', join(ws, 'fragment.json')], { stdio: 'ignore', env: e });
  execFileSync(NODE, [S('cluster3.mjs')], { stdio: 'ignore', env: e });
  execFileSync(NODE, [S('overlap.mjs')], { stdio: 'ignore', env: e });
  const g = JSON.parse(readFileSync(join(ws, 'graph.json'), 'utf8'));
  rmrf(ws);
  return g;
}

// =================================================================================================
// H9 — Type-1 exact clone detection (precision / recall / F1 vs name-match-only baseline)
// =================================================================================================
// Synthetic design (exact ground truth):
//   - GENUINE clone cluster: a unique function NAME defined in m>=2 files with BYTE-IDENTICAL bodies
//     (a real Type-1 re-implementation). Ground-truth positive.
//   - COINCIDENTAL cluster: a unique function NAME defined in m>=2 files with DISTINCT bodies
//     (same name, different logic). Ground-truth negative — a name-match baseline's false positive.
//   - BASE noise: singleton distinct functions (one file each), never clustered.
//   Names are disjoint across clusters so overlap.mjs's by-label grouping maps 1:1 to a planted cluster.
//
// codeweb POSITIVE = an overlap finding (duplicate-logic) for that name with confidence in
//   {high, medium} (overlap.mjs's reported "findings" band). refuted/low = codeweb-dismissed.
// BASELINE POSITIVE = any same-name multi-file cluster (name-match-only, no body confirmation).
//   -> baseline flags BOTH genuine and coincidental clusters (recall 1, precision = genuine/all).
function genCloneCorpus(rng, K) {
  const srcDir = freshTmp('cw-h9-');
  const planted = []; // {name, kind:'genuine'|'coincidental', files:[...]}
  let fileNo = 0;
  const distinctBodyFor = (name, salt) => { const r = prng((salt * 2654435761) >>> 0); return makeBody(r, name, ipick(r, 4, 8)); };
  for (let i = 0; i < K; i++) {
    const name = `clone_${i}`;
    const m = ipick(rng, 2, 3);
    const body = makeBody(rng, name, ipick(rng, 4, 8));
    const files = [];
    for (let j = 0; j < m; j++) { const rel = `g${i}_${j}/mod.js`; writeFile(srcDir, rel, 'export ' + body + '\n'); files.push(`${rel}:${name}`); }
    planted.push({ name, kind: 'genuine', files });
  }
  for (let i = 0; i < K; i++) {
    const name = `coin_${i}`;
    const m = ipick(rng, 2, 3);
    const files = [];
    for (let j = 0; j < m; j++) { const rel = `c${i}_${j}/mod.js`; writeFile(srcDir, rel, 'export ' + distinctBodyFor(name, i * 100 + j + 1) + '\n'); files.push(`${rel}:${name}`); }
    planted.push({ name, kind: 'coincidental', files });
  }
  // base noise: distinct singletons
  for (let i = 0; i < K; i++) { const name = `base_${i}`; writeFile(srcDir, `base/u${i}.js`, 'export ' + makeBody(rng, name, ipick(rng, 3, 6)) + '\n'); }
  return { srcDir, planted };
}

// Score one corpus graph against its planted record. Returns per-method confusion counts.
function scoreH9(graph, planted) {
  const byName = new Map(planted.map((p) => [p.name, p]));
  const genuineNames = new Set(planted.filter((p) => p.kind === 'genuine').map((p) => p.name));
  const coinNames = new Set(planted.filter((p) => p.kind === 'coincidental').map((p) => p.name));
  const totalPositiveTruth = genuineNames.size; // ground-truth duplicate clusters

  // codeweb positives: duplicate-logic findings (high|medium) whose label is a planted cluster name.
  const cw = { tp: 0, fp: 0 };
  const cwFlagged = new Set();
  for (const o of graph.overlaps) {
    if (o.kind !== 'duplicate-logic') continue;
    const name = o.title.match(/`([^`]+)`/)?.[1] || o.nodes[0]?.split(':').pop();
    if (!byName.has(name)) continue;
    if (o.confidence === 'high' || o.confidence === 'medium') { cwFlagged.add(name); if (genuineNames.has(name)) cw.tp++; else cw.fp++; }
  }
  const cwFn = totalPositiveTruth - [...cwFlagged].filter((n) => genuineNames.has(n)).length;

  // baseline positives: every same-name multi-file cluster (name-match only). Derive from the graph's
  // own node label multiplicity so both methods see the SAME extracted symbol set.
  const filesByLabel = new Map();
  for (const n of graph.nodes) {
    if (n.kind === 'module') continue;
    if (!byName.has(n.label)) continue; // restrict to planted names (the controlled universe)
    if (!filesByLabel.has(n.label)) filesByLabel.set(n.label, new Set());
    filesByLabel.get(n.label).add(n.file);
  }
  const base = { tp: 0, fp: 0 };
  let baseFlaggedGenuine = 0;
  for (const [name, files] of filesByLabel) {
    if (files.size < 2) continue; // name-match needs the name in >=2 files
    if (genuineNames.has(name)) { base.tp++; baseFlaggedGenuine++; } else if (coinNames.has(name)) base.fp++;
  }
  const baseFn = totalPositiveTruth - baseFlaggedGenuine;
  return { cw: { ...cw, fn: cwFn }, base: { ...base, fn: baseFn }, truthPos: totalPositiveTruth };
}

function prf(tp, fp, fn) {
  const p = tp + fp > 0 ? tp / (tp + fp) : 0;
  const r = tp + fn > 0 ? tp / (tp + fn) : 0;
  const f1 = p + r > 0 ? (2 * p * r) / (p + r) : 0;
  return { p, r, f1 };
}
// Wilson CI on F1 is not standard; the pre-reg specifies Wilson CIs on the proportions. We report
// Wilson CIs on precision and recall, and derive an F1 interval by the (conservative) combination of
// the proportion-interval endpoints: F1lo from (p.lo, r.lo), F1hi from (p.hi, r.hi). This bounds F1
// using only Wilson intervals (no normal-approx on F1 itself) — disclosed in the results.
function f1Interval(tpP, nP, tpR, nR) {
  const wp = wilson(tpP, nP), wr = wilson(tpR, nR);
  const f1 = (p, r) => (p + r > 0 ? (2 * p * r) / (p + r) : 0);
  return { est: f1(wp.p, wr.p), lo: f1(wp.lo, wr.lo), hi: f1(wp.hi, wr.hi), precision: wp, recall: wr };
}

function runH9() {
  const Ks = [20, 50, 100];
  const SEEDS = 10;
  const SEED_BASE = 0x9C10;
  // aggregate confusion across all seeds/Ks (the family the pre-reg scores)
  let cwTP = 0, cwFP = 0, cwFN = 0, baseTP = 0, baseFP = 0, baseFN = 0;
  const perCell = [];
  let cliChecked = 0;
  for (const K of Ks) {
    for (let s = 0; s < SEEDS; s++) {
      const seed = (SEED_BASE ^ (K * 131 + s)) >>> 0;
      const rng = prng(seed);
      const { srcDir, planted } = genCloneCorpus(rng, K);
      try {
        const graph = runPipeline(srcDir, `h9-K${K}-s${s}`);
        const sc = scoreH9(graph, planted);
        cwTP += sc.cw.tp; cwFP += sc.cw.fp; cwFN += sc.cw.fn;
        baseTP += sc.base.tp; baseFP += sc.base.fp; baseFN += sc.base.fn;
        perCell.push({ K, seed, cw: sc.cw, base: sc.base });
        cliChecked++;
      } finally { rmrf(srcDir); }
    }
  }
  const cw = prf(cwTP, cwFP, cwFN), base = prf(baseTP, baseFP, baseFN);
  // Wilson CIs: precision over (tp+fp) decisions, recall over (tp+fn) truths.
  const cwF1 = f1Interval(cwTP, cwTP + cwFP, cwTP, cwTP + cwFN);
  const baseF1 = f1Interval(baseTP, baseTP + baseFP, baseTP, baseTP + baseFN);
  const passSynthetic = cwF1.lo > baseF1.hi;

  // ---- KNOWN-FAILING PROBE (H9 must be able to fail): an all-coincidental corpus where codeweb
  // SHOULD have ~0 true positives. If we (wrongly) scored coincidental clusters as ground-truth
  // positives, codeweb's recall would spuriously look perfect. The probe asserts codeweb does NOT
  // flag a high/medium finding for a coincidental-only corpus (i.e. precision metric can drop).
  const probeRng = prng(0xBADF00D);
  const probe = (() => {
    const srcDir = freshTmp('cw-h9probe-');
    const names = [];
    for (let i = 0; i < 8; i++) {
      const name = `coin_${i}`; names.push(name);
      for (let j = 0; j < 2; j++) { const r = prng((i * 99991 + j + 1) >>> 0); writeFile(srcDir, `c${i}_${j}/m.js`, 'export ' + makeBody(r, name, ipick(r, 4, 8)) + '\n'); }
    }
    try {
      const g = runPipeline(srcDir, 'h9-probe');
      let highMed = 0;
      for (const o of g.overlaps) if (o.kind === 'duplicate-logic' && (o.confidence === 'high' || o.confidence === 'medium') && names.includes(o.title.match(/`([^`]+)`/)?.[1])) highMed++;
      // overlap should refute/low these; a harness that can't fail would still pass if it ignored bodies.
      return { coincidentalHighMedFindings: highMed, ableToFail: highMed === 0 };
    } finally { rmrf(srcDir); }
  })();

  // ---- axios real-world precision (external validity; body-confirmed human-style labels) ----
  const axios = runAxiosPrecision();

  return {
    cw: { precision: round(cw.p), recall: round(cw.r), f1: round(cw.f1), tp: cwTP, fp: cwFP, fn: cwFN, f1CI: { lo: round(cwF1.lo), hi: round(cwF1.hi) }, precisionCI: { lo: round(cwF1.precision.lo), hi: round(cwF1.precision.hi) }, recallCI: { lo: round(cwF1.recall.lo), hi: round(cwF1.recall.hi) } },
    baseline: { precision: round(base.p), recall: round(base.r), f1: round(base.f1), tp: baseTP, fp: baseFP, fn: baseFN, f1CI: { lo: round(baseF1.lo), hi: round(baseF1.hi) }, precisionCI: { lo: round(baseF1.precision.lo), hi: round(baseF1.precision.hi) } },
    Ks, seedsPerK: SEEDS, cellsRun: perCell.length, cliPipelinesRun: cliChecked,
    axios, probe, passSynthetic,
    pass: passSynthetic, // headline pass is the synthetic significance test
  };
}

// axios: run overlap on the real corpus, classify each finding by labels-axios.json (body-confirmed).
function runAxiosPrecision() {
  const axiosDir = join(ROOT, 'paper', 'corpus', 'axios');
  if (!existsSync(axiosDir)) return { available: false, note: 'paper/corpus/axios not present — run clone-corpus.sh' };
  const labels = JSON.parse(readFileSync(join(RESULTS, 'labels-axios.json'), 'utf8'));
  const verdict = new Map(labels.findings.map((f) => [f.finding, f.verdict])); // codeweb-finding verdicts
  // dismissed-tier verdicts: name-match flags these too; label them for the baseline precision.
  // Body-confirmed conservative rule (disclosed): the clearly-genuine same-named helpers are true,
  // everything else codeweb refuted/low is coincidental.
  const dismissedTrue = new Set(['ov26', 'ov60', 'ov62']); // own / pipeFileToResponse / _resolve: real drifted helpers
  const graph = runPipeline(axiosDir, 'axios');
  const cands = graph.overlaps.filter((o) => o.kind === 'duplicate-logic' || o.kind === 'parallel-impl');
  // codeweb findings = high|medium
  let cwTP = 0, cwFP = 0; const unlabeled = [];
  for (const o of cands) {
    if (o.confidence !== 'high' && o.confidence !== 'medium') continue;
    const v = verdict.get(o.id);
    if (v == null) { unlabeled.push(o.id); continue; }
    if (v === 'true-duplicate') cwTP++; else cwFP++;
  }
  // baseline = ALL same-name multi-file clusters it would flag = every candidate cluster.
  let baseTP = 0, baseFP = 0;
  for (const o of cands) {
    let isTrue;
    if (verdict.has(o.id)) isTrue = verdict.get(o.id) === 'true-duplicate';
    else isTrue = dismissedTrue.has(o.id); // dismissed tier
    if (isTrue) baseTP++; else baseFP++;
  }
  const cwP = cwTP + cwFP > 0 ? cwTP / (cwTP + cwFP) : 0;
  const baseP = baseTP + baseFP > 0 ? baseTP / (baseTP + baseFP) : 0;
  const cwW = wilson(cwTP, cwTP + cwFP), baseW = wilson(baseTP, baseTP + baseFP);
  return {
    available: true,
    disclosure: 'External validity, NOT exact truth: labels are body-confirmed human-style judgments (labels-axios.json). Real-repo recall is unknowable exactly; precision is the reportable real-world number. The headline statistical PASS for H9 is the SYNTHETIC F1 contrast.',
    codeweb: { precision: round(cwP), tp: cwTP, fp: cwFP, findings: cwTP + cwFP, precisionCI: { lo: round(cwW.lo), hi: round(cwW.hi) }, unlabeled },
    baseline: { precision: round(baseP), tp: baseTP, fp: baseFP, flagged: baseTP + baseFP, precisionCI: { lo: round(baseW.lo), hi: round(baseW.hi) } },
    precisionGain: round(cwP - baseP),
  };
}

// =================================================================================================
// H10 — Type-2 (renamed) clone recall: find-similar --structural recall > lexical recall.
// =================================================================================================
// Design: plant N base functions (each in its own file). For each, the QUERY is its identifier-
// renamed Type-2 clone (renameBody). Ground truth = the base function is a clone of the query. A
// method RECALLS the clone iff the base appears among the method's matches (sim >= 0.15, the tool's
// reporting floor). Per-seed recall = (# bases recalled)/N for structural and for lexical. Paired by
// seed; paired-difference bootstrap CI must exclude 0 (structural strictly higher).
function genType2Corpus(rng, N) {
  const srcDir = freshTmp('cw-h10-');
  const bases = []; // {id, name, file, body, query}
  for (let i = 0; i < N; i++) {
    const name = `base_${i}`;
    const body = makeBody(rng, name, ipick(rng, 5, 9));
    const file = `b${i}/mod.js`;
    writeFile(srcDir, file, body + '\n');
    const query = renameBody(body, i + 1);
    bases.push({ id: `${file}:${name}`, name, file, body, query });
  }
  return { srcDir, bases };
}
// In-process recall using the same shingle engines the CLI uses (mirrors the tool); the CLI is then
// cross-checked on a sample. Ground truth (clone identity) is independent of both.
function inProcRecall(bases, mode) {
  const shg = mode === 'structural' ? (s) => structuralShingles(s, 3) : (s) => lexShingles(s, 3);
  let recalled = 0;
  for (const q of bases) {
    const cand = shg(q.query);
    let best = 0;
    for (const b of bases) { const sim = jacc(cand, shg(b.body)); if (b.id === q.id && sim > best) best = sim; }
    // recall counts the TRUE base for this query (b.id === q.id) being above the 0.15 floor.
    const trueSim = jacc(cand, shg(bases.find((b) => b.id === q.id).body));
    if (trueSim >= 0.15) recalled++;
  }
  return recalled / bases.length;
}

function runH10() {
  const N = 30, SEEDS = 10, SEED_BASE = 0xA1020;
  const pairs = []; // {before: lexical, after: structural}
  const perSeed = [];
  for (let s = 0; s < SEEDS; s++) {
    const seed = (SEED_BASE ^ (s * 977)) >>> 0;
    const rng = prng(seed);
    const { srcDir, bases } = genType2Corpus(rng, N);
    try {
      const lex = inProcRecall(bases, 'lexical');
      const str = inProcRecall(bases, 'structural');
      pairs.push({ before: lex, after: str });
      perSeed.push({ seed, lexicalRecall: round(lex), structuralRecall: round(str) });
    } finally { rmrf(srcDir); }
  }
  const diffCI = pairedDiffCI(pairs, { B: 10000, seed: 0xC0FFEE });
  const meanLex = mean(pairs.map((p) => p.before)), meanStr = mean(pairs.map((p) => p.after));
  const passStat = diffCI.lo > 0; // structural strictly higher (CI excludes 0)

  // ---- CLI cross-check: confirm the shipped find-similar agrees on a sample (one seed). The tool
  // must (a) recall the renamed clone in --structural and (b) NOT recall it lexically for at least
  // some bases — i.e. the in-process recall mirrors the real artifact.
  const cliSeed = 0xA10C01 >>> 0;
  const cliRng = prng(cliSeed);
  const { srcDir, bases } = genType2Corpus(cliRng, 12);
  let cliStrRecalled = 0, cliLexRecalled = 0;
  try {
    const ws = freshTmp('cw-h10-ws-');
    writeFileSync(join(ws, 'graph.json'), JSON.stringify(buildGraphFromBases(srcDir, bases)));
    for (const q of bases) {
      const candPath = join(ws, 'cand.txt'); writeFileSync(candPath, q.query);
      const str = JSON.parse(execFileSync(NODE, [S('find-similar.mjs'), join(ws, 'graph.json'), '--body', candPath, '--structural', '--k', '50', '--json'], { encoding: 'utf8' }));
      const lex = JSON.parse(execFileSync(NODE, [S('find-similar.mjs'), join(ws, 'graph.json'), '--body', candPath, '--k', '50', '--json'], { encoding: 'utf8' }));
      if (str.matches.some((m) => m.id === q.id)) cliStrRecalled++;
      if (lex.matches.some((m) => m.id === q.id)) cliLexRecalled++;
    }
    rmrf(ws);
  } finally { rmrf(srcDir); }

  // ---- KNOWN-FAILING PROBE: feed find-similar a body that is NOT a clone of anything (random
  // distinct function). Structural recall of a non-existent clone must be 0 — proves recall can be
  // less than 1 (the metric is not vacuously saturated).
  const probeRng = prng(0xDEADBEAF);
  const probeBody = makeBody(probeRng, 'orphanQuery', 7);
  const { srcDir: pdir, bases: pbases } = genType2Corpus(prng(0x515), 6);
  let probeRecalled = false;
  try {
    const ws = freshTmp('cw-h10p-'); writeFileSync(join(ws, 'graph.json'), JSON.stringify(buildGraphFromBases(pdir, pbases)));
    const cp = join(ws, 'c.txt'); writeFileSync(cp, probeBody);
    const out = JSON.parse(execFileSync(NODE, [S('find-similar.mjs'), join(ws, 'graph.json'), '--body', cp, '--structural', '--k', '50', '--json'], { encoding: 'utf8' }));
    probeRecalled = out.matches.some((m) => m.sim >= 0.9); // a near-perfect structural match would be spurious
    rmrf(ws);
  } finally { rmrf(pdir); }

  return {
    N, seeds: SEEDS,
    lexicalRecallMean: round(meanLex), structuralRecallMean: round(meanStr),
    pairedDiffMean: round(diffCI.est), pairedDiffCI: { lo: round(diffCI.lo), hi: round(diffCI.hi) },
    perSeed,
    cliCrossCheck: { bases: bases.length, structuralRecalled: cliStrRecalled, lexicalRecalled: cliLexRecalled, agrees: cliStrRecalled > cliLexRecalled },
    probe: { unrelatedQueryGotSpuriousStructuralMatch: probeRecalled, ableToFail: probeRecalled === false },
    pass: passStat,
  };
}

function buildGraphFromBases(srcDir, bases) {
  const nodes = bases.map((b) => ({ id: b.id, label: b.name, kind: 'function', file: b.file, line: 1, loc: b.body.split('\n').length, exports: false, domain: 'd' }));
  return { meta: { root: srcDir.replace(/\\/g, '/'), target: 'h10' }, nodes, edges: [], domains: [], overlaps: [] };
}

// =================================================================================================
// H11 — find-similar ranking quality: MRR, recall@1, recall@5 vs a random baseline.
// =================================================================================================
// Design: a corpus of M distinct functions, each in its own file. For each, query find-similar with
// an identifier-renamed copy (Type-2) of that function and find the RANK of the true source in the
// returned (structural) ranking. MRR = mean(1/rank) (rank=inf -> 0). Random baseline: the expected
// reciprocal rank of a uniformly-random position among M candidates. PASS: MRR >= 0.8 AND the
// bootstrap CI of MRR is strictly above the random-baseline MRR.
function runH11() {
  const M = 40, SEEDS = 10, SEED_BASE = 0xB1130;
  const rrAll = []; // reciprocal ranks across all queries/seeds (for MRR + its CI)
  let hit1 = 0, hit5 = 0, total = 0;
  const perSeed = [];
  let cliChecked = 0, cliAgree = 0;
  for (let s = 0; s < SEEDS; s++) {
    const seed = (SEED_BASE ^ (s * 1409)) >>> 0;
    const rng = prng(seed);
    const { srcDir, bases } = genType2Corpus(rng, M);
    try {
      const ws = freshTmp('cw-h11-ws-');
      writeFileSync(join(ws, 'graph.json'), JSON.stringify(buildGraphFromBases(srcDir, bases)));
      let seedHit1 = 0, seedHit5 = 0;
      // CLI cross-check on a sample of 6 queries per seed (keep total CLI spawns feasible).
      const sample = new Set(); { const r2 = prng((seed ^ 0x55) >>> 0); while (sample.size < 6) sample.add((r2() * bases.length) | 0); }
      bases.forEach((q, qi) => {
        const candPath = join(ws, 'cand.txt'); writeFileSync(candPath, q.query);
        const out = JSON.parse(execFileSync(NODE, [S('find-similar.mjs'), join(ws, 'graph.json'), '--body', candPath, '--structural', '--k', String(M + 5), '--json'], { encoding: 'utf8' }));
        const rank = out.matches.findIndex((m) => m.id === q.id) + 1; // 1-based; 0 -> not found
        const rr = rank > 0 ? 1 / rank : 0;
        rrAll.push(rr); total++;
        if (rank === 1) { hit1++; seedHit1++; }
        if (rank >= 1 && rank <= 5) { hit5++; seedHit5++; }
        if (sample.has(qi)) { cliChecked++; if (rank === 1) cliAgree++; }
      });
      perSeed.push({ seed, recallAt1: round(seedHit1 / M), recallAt5: round(seedHit5 / M) });
      rmrf(ws);
    } finally { rmrf(srcDir); }
  }
  const mrr = mean(rrAll);
  const mrrCI = bootstrapCI(rrAll, mean, { B: 10000, seed: 0xBEAD });
  // random baseline: query ranks the true source uniformly among M candidates -> E[1/rank] = H_M/M.
  const HM = Array.from({ length: M }, (_, i) => 1 / (i + 1)).reduce((a, b) => a + b, 0);
  const randomMRR = HM / M;
  const passStat = mrr >= 0.8 && mrrCI.lo > randomMRR;

  // ---- KNOWN-FAILING PROBE: a RANDOM-ranking simulation must FAIL the MRR>=0.8 bar — proves the bar
  // is discriminating (a non-ranking method cannot pass).
  const prng2 = prng(0xF00DCAFE);
  const randTrials = 5000; let randRR = [];
  for (let i = 0; i < randTrials; i++) { const rank = 1 + ((prng2() * M) | 0); randRR.push(1 / rank); }
  const randSimMRR = mean(randRR);
  const probeFailsBar = !(randSimMRR >= 0.8);

  return {
    M, seeds: SEEDS, queries: total,
    mrr: round(mrr), mrrCI: { lo: round(mrrCI.lo), hi: round(mrrCI.hi) },
    recallAt1: round(hit1 / total), recallAt5: round(hit5 / total),
    randomBaselineMRR: round(randomMRR),
    perSeed,
    cliCrossCheck: { sampled: cliChecked, rankedFirst: cliAgree, agreementRate: round(cliChecked ? cliAgree / cliChecked : 0) },
    probe: { randomRankingSimMRR: round(randSimMRR), randomRankingFailsBar: probeFailsBar, ableToFail: probeFailsBar },
    pass: passStat,
  };
}

// =================================================================================================
// H12 — false-hub avoidance (the byName[0] fix): shipped max-false-indeg <= 1/3 of legacy, every seed.
// =================================================================================================
// Design: a same-name-heavy corpus — D files each DEFINE a function `log` (multi-def, ambiguous),
// and C caller files each make a BARE `log(...)` call with NO import. Under the fix these ambiguous
// bare calls are DROPPED (no fabricated edge). Under CODEWEB_LEGACY_FALLBACK=1 they are wired to
// byName[0], fabricating a false super-hub. We count the MAX in-degree among the ambiguous-name
// nodes (the false hub) over the EXTRACTOR'S OWN emitted call edges (oracle = our own counter).
function genHubCorpus(rng, names, defsPer, callersPer) {
  const srcDir = freshTmp('cw-h12-');
  for (const name of names) {
    for (let i = 0; i < defsPer; i++) writeFile(srcDir, `def_${name}_${i}.js`, `export function ${name}(m) { return m + ${ipick(rng, 0, 9)}; }\n`);
    for (let i = 0; i < callersPer; i++) writeFile(srcDir, `call_${name}_${i}.js`, `export function use_${name}_${i}() {\n  ${name}("x${i}");\n}\n`);
  }
  return { srcDir, names };
}
function maxFalseIndeg(fragment, names) {
  const nameSet = new Set(names);
  const indeg = new Map();
  for (const e of fragment.edges) if (e.kind === 'call') indeg.set(e.to, (indeg.get(e.to) || 0) + 1);
  let mx = 0, id = null;
  for (const [to, d] of indeg) { const label = to.slice(to.lastIndexOf(':') + 1); if (nameSet.has(label) && d > mx) { mx = d; id = to; } }
  return { max: mx, node: id };
}
function extractFragment(srcDir, target, env = {}) {
  const ws = freshTmp('cw-h12-ws-');
  const e = { ...process.env, ...env, CODEWEB_WS: ws };
  execFileSync(NODE, [S('extract-symbols.mjs'), srcDir, '--no-ctags', '--target', target, '--out', join(ws, 'fragment.json')], { stdio: 'ignore', env: e });
  const frag = JSON.parse(readFileSync(join(ws, 'fragment.json'), 'utf8'));
  rmrf(ws);
  return frag;
}
function runH12() {
  const SEEDS = 10, SEED_BASE = 0xC1200;
  const names = ['log', 'parse', 'get', 'format', 'read'];
  const perSeed = [];
  let allPass = true;
  // The legacy fallback wires the false hub to byName[0] — and WHICH definition is [0] depends on
  // rg --files enumeration order, which is NOT stable across process invocations. So the legacy hub's
  // *node identity* is intentionally NOT recorded per-seed (it is the documented fragility itself);
  // only the in-degree MAGNITUDE (the metric) is deterministic. We sample it once for disclosure.
  for (let s = 0; s < SEEDS; s++) {
    const seed = (SEED_BASE ^ (s * 6121)) >>> 0;
    const rng = prng(seed);
    const callersPer = ipick(rng, 10, 30);
    const { srcDir } = genHubCorpus(rng, names, ipick(rng, 3, 6), callersPer);
    try {
      const shipped = maxFalseIndeg(extractFragment(srcDir, `h12-s${s}`), names);
      const legacy = maxFalseIndeg(extractFragment(srcDir, `h12-s${s}-leg`, { CODEWEB_LEGACY_FALLBACK: '1' }), names);
      const ok = shipped.max <= legacy.max / 3;
      if (!ok) allPass = false;
      perSeed.push({ seed, callersPer, shippedMaxFalseIndeg: shipped.max, legacyMaxFalseIndeg: legacy.max, ratio: round(legacy.max > 0 ? shipped.max / legacy.max : 0), pass: ok });
    } finally { rmrf(srcDir); }
  }
  // Disclosure: demonstrate the legacy hub's node-identity nondeterminism on ONE fixed corpus across
  // two extractions (the magnitude is stable; the chosen [0] node may differ run-to-run).
  let legacyNodeNondeterminism = null;
  {
    const rng = prng(0xC12FACE);
    const { srcDir } = genHubCorpus(rng, ['log'], 5, 30);
    try {
      const n1 = maxFalseIndeg(extractFragment(srcDir, 'h12-nd1', { CODEWEB_LEGACY_FALLBACK: '1' }), ['log']);
      const n2 = maxFalseIndeg(extractFragment(srcDir, 'h12-nd2', { CODEWEB_LEGACY_FALLBACK: '1' }), ['log']);
      legacyNodeNondeterminism = { run1Node: n1.node, run2Node: n2.node, magnitudeStable: n1.max === n2.max, magnitude: n1.max, nodeIdentityVaried: n1.node !== n2.node };
    } finally { rmrf(srcDir); }
  }
  // ---- KNOWN-FAILING PROBE: under LEGACY the false hub must be LARGE (>= callers) — proves the
  // scenario actually fabricates a hub (so "shipped is small" is a real win, not an empty corpus).
  const probeRng = prng(0xC0DEC0DE);
  const { srcDir } = genHubCorpus(probeRng, ['log'], 4, 25);
  let legacyHubLarge = false;
  try { const legacy = maxFalseIndeg(extractFragment(srcDir, 'h12-probe', { CODEWEB_LEGACY_FALLBACK: '1' }), ['log']); legacyHubLarge = legacy.max >= 20; }
  finally { rmrf(srcDir); }

  return {
    seeds: SEEDS, names, perSeed,
    legacyNodeNondeterminism,
    goldenRealTarget: 'discord:log 127->2 (documented in tests/golden-ecc-scripts.test.mjs; that repo is outside this study corpus, so the synthetic same-name-heavy corpus stands in for the controlled A/B; the golden test is the real-world anchor).',
    probe: { legacyFabricatesLargeHub: legacyHubLarge, ableToFail: legacyHubLarge },
    pass: allPass,
  };
}

// =================================================================================================
// H13 — deadcode safe-tier precision vs an INDEPENDENT reachability analysis.
// =================================================================================================
// INDEPENDENT ORACLE (from scratch, no graph-ops import): forward-reachability over call|import|
// inherit edges from ROOTS. A symbol is "truly dead" iff NOT reachable from any root. Roots:
//   (1) entrypoint-like NAMES (a framework/CLI invokes them without a code edge),
//   (2) EXPORTED symbols (public API — reachable from outside the module graph),
//   (3) TEST functions (a test runner invokes them; a test file's own functions are roots).
// deadcode.mjs's "safe to delete" tier should contain ONLY truly-dead symbols. We measure the safe
// tier's PRECISION = (safe ∩ trulyDead)/safe, and report RECALL = (safe ∩ trulyDead)/trulyDead to
// expose the deliberate precision-over-recall tradeoff. The graphs are synthetic with planted
// reachable structure + planted orphans + test-only refs + test functions (which STRESS precision).
const ENTRYPOINT_NAMES = new Set(['main', 'default', 'index', 'setup', 'teardown', 'init']);
const isTestPath = (file) => /(?:^|\/)(?:tests?|__tests__)\//.test(file || '') || /(?:\.test\.|\.spec\.|_test\.)/.test(file || '');
function reachableTrulyDead(graph) {
  const byId = new Map(graph.nodes.map((n) => [n.id, n]));
  const fwd = new Map(); // forward adjacency over call|import|inherit
  for (const e of graph.edges) {
    if (e.kind !== 'call' && e.kind !== 'import' && e.kind !== 'inherit') continue;
    if (!fwd.has(e.from)) fwd.set(e.from, new Set());
    fwd.get(e.from).add(e.to);
  }
  const roots = [];
  for (const n of graph.nodes) {
    if (n.kind === 'module') continue;
    if (ENTRYPOINT_NAMES.has(n.label) || n.exports || isTestPath(n.file)) roots.push(n.id);
  }
  const reach = new Set(roots);
  const q = [...roots];
  while (q.length) { const cur = q.shift(); for (const nxt of (fwd.get(cur) || [])) if (!reach.has(nxt)) { reach.add(nxt); q.push(nxt); } }
  const trulyDead = new Set();
  for (const n of graph.nodes) { if (n.kind === 'module') continue; if (!reach.has(n.id)) trulyDead.add(n.id); }
  return { trulyDead, reach };
}

// Synthetic graph generator for H13. Plants: a reachable core (entrypoint main -> chain), exported
// API (reachable), test functions referencing some prod symbols, planted TRUE orphans (dead), and
// "reachable-but-uncalled" distractors (exported or reached via import) that must NOT be 'safe'.
function genDeadcodeGraph(rng, nCore, nDead, nTestFns) {
  const nodes = []; const edges = [];
  const mk = (id, label, file, exports = false, kind = 'function') => { nodes.push({ id, label, kind, file, line: nodes.length + 1, loc: 1, exports, domain: 'd' }); return id; };
  // reachable core: main -> c0 -> c1 -> ... (a call chain)
  const main = mk('app.js:main', 'main', 'app.js', false);
  let prev = main;
  for (let i = 0; i < nCore; i++) { const id = mk(`app.js:core${i}`, `core${i}`, 'app.js', rng() < 0.3); edges.push({ from: prev, to: id, kind: 'call' }); prev = id; }
  // an exported-but-uncalled API symbol (reachable as a root because exported) — must be review/excluded, not safe-dead
  const api = mk('api.js:publicApi', 'publicApi', 'api.js', true);
  // a symbol reached only via import edge from main's file anchor (reachable, not dead)
  const importTarget = mk('helpers.js:helperX', 'helperX', 'helpers.js', false);
  edges.push({ from: main, to: importTarget, kind: 'import' });
  // entrypoint-like-named uncalled symbol (reachable root by name) — deadcode -> review, oracle -> reachable
  mk('boot.js:init', 'init', 'boot.js', false);
  // planted TRUE orphans: no incoming edges, not exported, not entrypoint-named, in non-test files
  const deadIds = [];
  for (let i = 0; i < nDead; i++) deadIds.push(mk(`dead.js:dead${i}`, `dead${i}`, 'dead.js', false));
  // test functions: live in a test file, reference some prod symbols via 'test' edges. The test fns
  // THEMSELVES have no incoming edge + not exported -> deadcode may put them in SAFE (precision risk),
  // but the oracle treats them as ROOTS (a test runner invokes them) -> NOT dead. This is the crux.
  for (let i = 0; i < nTestFns; i++) {
    const tid = mk(`suite.test.js:t${i}`, `t${i}`, 'suite.test.js', false);
    // wire a test edge to a random core symbol (exercises it) — this is how the extractor emits test edges
    const tgt = nodes[1 + ((rng() * Math.max(1, nCore)) | 0)];
    if (tgt) edges.push({ from: tid, to: tgt.id, kind: 'test' });
  }
  return { graph: { meta: {}, nodes, edges, domains: [], overlaps: [] }, deadIds };
}

function runDeadcodeCLI(graph) {
  const dir = freshTmp('cw-h13-');
  const gp = join(dir, 'graph.json'); writeFileSync(gp, JSON.stringify(graph));
  try { return JSON.parse(execFileSync(NODE, [S('deadcode.mjs'), gp, '--json'], { encoding: 'utf8' })); }
  finally { rmrf(dir); }
}

function runH13() {
  const SEEDS = 10, SEED_BASE = 0xD1300, GRAPHS_PER_SEED = 6;
  let safeTP = 0, safeFP = 0; // precision numerator/denominator (PRE-REGISTERED metric: all safe items)
  let deadCoveredBySafe = 0, totalTrulyDead = 0; // recall
  // EXPLORATORY diagnostic (clearly labeled, NOT the pre-registered metric): precision if the safe
  // tier excluded functions DEFINED IN TEST FILES (the dominant false-positive class). This isolates
  // the failure mechanism without changing the pass criterion.
  let prodTP = 0, prodFP = 0;
  const perSeed = [];
  let cliRun = 0;
  const fpExamples = [];
  const fpByClass = { testFileFn: 0, other: 0 };
  for (let s = 0; s < SEEDS; s++) {
    const seed = (SEED_BASE ^ (s * 7333)) >>> 0;
    const rng = prng(seed);
    let sTP = 0, sFP = 0, dCov = 0, dTot = 0;
    for (let g = 0; g < GRAPHS_PER_SEED; g++) {
      const { graph } = genDeadcodeGraph(rng, ipick(rng, 3, 8), ipick(rng, 1, 5), ipick(rng, 1, 4));
      const out = runDeadcodeCLI(graph); cliRun++;
      const { trulyDead } = reachableTrulyDead(graph);
      const byId = new Map(graph.nodes.map((n) => [n.id, n]));
      const safeIds = out.safe.map((x) => x.id);
      for (const id of safeIds) {
        const inTestFile = isTestPath(byId.get(id)?.file);
        if (trulyDead.has(id)) { sTP++; if (!inTestFile) prodTP++; }
        else {
          sFP++;
          if (inTestFile) fpByClass.testFileFn++; else fpByClass.other++;
          if (!inTestFile) prodFP++; // exploratory cut drops test-file FPs
          if (fpExamples.length < 8) fpExamples.push(id);
        }
      }
      // recall: how many truly-dead are in safe
      const safeSet = new Set(safeIds);
      for (const d of trulyDead) { dTot++; if (safeSet.has(d)) dCov++; }
    }
    safeTP += sTP; safeFP += sFP; deadCoveredBySafe += dCov; totalTrulyDead += dTot;
    perSeed.push({ seed, safeTP: sTP, safeFP: sFP, precision: round(sTP + sFP ? sTP / (sTP + sFP) : 1), recall: round(dTot ? dCov / dTot : 1) });
  }
  const precision = safeTP + safeFP > 0 ? safeTP / (safeTP + safeFP) : 1;
  const recall = totalTrulyDead > 0 ? deadCoveredBySafe / totalTrulyDead : 1;
  const w = wilson(safeTP, safeTP + safeFP);
  const passStat = w.lo >= 0.95;
  const prodPrecision = prodTP + prodFP > 0 ? prodTP / (prodTP + prodFP) : 1;
  const wProd = wilson(prodTP, prodTP + prodFP);

  // ---- KNOWN-FAILING PROBE: a graph where a TEST FUNCTION is the only orphan. The oracle says it
  // is reachable (test runner root) => NOT dead; if deadcode marks it 'safe', that is a precision
  // MISS the harness MUST count as a false positive. We assert the harness DOES flag it (i.e. the
  // metric is sensitive to this real failure mode), regardless of whether it currently happens.
  const probeGraph = {
    meta: {}, domains: [], overlaps: [],
    nodes: [
      { id: 'm.js:main', label: 'main', kind: 'function', file: 'm.js', line: 1, loc: 1, exports: false, domain: 'd' },
      { id: 'm.js:helper', label: 'helper', kind: 'function', file: 'm.js', line: 2, loc: 1, exports: true, domain: 'd' },
      { id: 'only.test.js:tCase', label: 'tCase', kind: 'function', file: 'only.test.js', line: 1, loc: 1, exports: false, domain: 'd' },
    ],
    edges: [{ from: 'only.test.js:tCase', to: 'm.js:helper', kind: 'test' }],
  };
  const probeOut = runDeadcodeCLI(probeGraph);
  const { trulyDead: probeDead } = reachableTrulyDead(probeGraph);
  const probeSafe = probeOut.safe.map((x) => x.id);
  // Does deadcode put the test fn in safe, and would the oracle disagree (count it FP)?
  const testFnInSafe = probeSafe.includes('only.test.js:tCase');
  const oracleSaysReachable = !probeDead.has('only.test.js:tCase'); // test fn is a root -> reachable
  const harnessWouldCountFP = testFnInSafe && oracleSaysReachable;

  // ---- REAL-REPO SPOT-CHECK (pre-reg asks for it): run the shipped pipeline + deadcode on real
  // corpus repos and report how much of the 'safe to delete' tier is functions DEFINED IN TEST FILES
  // (which a test runner invokes -> not dead). This confirms the synthetic failure is the REAL
  // mechanism, not a synthetic artifact.
  const realSpot = realRepoDeadcodeSpotCheck(['axios', 'express', 'flask', 'gorilla-mux']);

  return {
    seeds: SEEDS, graphsPerSeed: GRAPHS_PER_SEED, cliRuns: cliRun,
    safePrecision: round(precision), safePrecisionCI: { lo: round(w.lo), hi: round(w.hi) },
    safeRecall: round(recall),
    safeTP, safeFP, totalTrulyDead, deadCoveredBySafe,
    falsePositiveBreakdown: fpByClass,
    falsePositiveExamples: fpExamples,
    exploratory_productionOnlySafeTier: {
      note: 'EXPLORATORY (not the pre-registered metric): precision of the safe tier if functions defined in TEST FILES were excluded. Isolates the failure mechanism. The pre-registered metric (above) still governs PASS/FAIL.',
      precision: round(prodPrecision), precisionCI: { lo: round(wProd.lo), hi: round(wProd.hi) }, prodTP, prodFP,
    },
    realRepoSpotCheck: realSpot,
    oracle: 'INDEPENDENT forward-reachability BFS (roots = entrypoint-like names + exported + test functions); written from scratch in this harness, does NOT import graph-ops.orphans.',
    findingSummary: 'H13 FAILS as shipped: the deadcode "safe to delete" tier does NOT exclude functions DEFINED IN TEST FILES. Such functions have no incoming code edge, are not exported, and are not entrypoint-named, so deadcode.mjs files them as safe — but a test runner invokes them, so the independent reachability oracle (correctly) counts them reachable. On real repos this dominates the safe tier (e.g. express 928/932 safe items are test-file functions like `it`). The deliberate precision-over-recall design holds for PRODUCTION symbols (see exploratory cut), but the safe tier is unsafe to apply on a repo containing tests.',
    probe: { testFnInSafe, oracleSaysReachable, harnessWouldCountAsFalsePositive: harnessWouldCountFP, ableToFail: testFnInSafe && oracleSaysReachable },
    pass: passStat,
  };
}

// Run extract->cluster->deadcode on real repos; quantify test-file pollution of the safe tier.
function realRepoDeadcodeSpotCheck(repos) {
  const out = {};
  for (const repo of repos) {
    const dir = join(ROOT, 'paper', 'corpus', repo);
    if (!existsSync(dir)) { out[repo] = { available: false }; continue; }
    const ws = freshTmp('cw-h13real-');
    const env = { ...process.env, CODEWEB_WS: ws };
    try {
      execFileSync(NODE, [S('extract-symbols.mjs'), dir, '--no-ctags', '--target', repo, '--out', join(ws, 'fragment.json')], { stdio: 'ignore', env });
      execFileSync(NODE, [S('cluster3.mjs')], { stdio: 'ignore', env });
      const g = JSON.parse(readFileSync(join(ws, 'graph.json'), 'utf8'));
      const dc = JSON.parse(execFileSync(NODE, [S('deadcode.mjs'), join(ws, 'graph.json'), '--json'], { encoding: 'utf8' }));
      const byId = new Map(g.nodes.map((n) => [n.id, n]));
      const safeTestFns = dc.safe.filter((s) => isTestPath(byId.get(s.id)?.file));
      out[repo] = {
        available: true, totalSafe: dc.safe.length,
        safeInTestFiles: safeTestFns.length, safeInProduction: dc.safe.length - safeTestFns.length,
        testFilePollutionRate: round(dc.safe.length ? safeTestFns.length / dc.safe.length : 0),
        sampleTestFileSafe: safeTestFns.slice(0, 4).map((s) => s.id),
      };
    } catch (e) { out[repo] = { available: true, error: String(e.message || e).slice(0, 200) }; }
    finally { rmrf(ws); }
  }
  return out;
}

